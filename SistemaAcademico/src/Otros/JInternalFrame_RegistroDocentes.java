    private void jTextField_NombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_NombreKeyReleased
        // TODO add your handling code here:
        String[] titulos = {"Codigo Alumno", "Nombres", "Apellidos", "DNI", "Fecha Nacimiento", "Direccion", "Sexo", "Telefono", "Estado del Alumno"};
        String[] registros = new String[200];
        String sql = "SELECT * FROM talumno WHERE NombreAlumno LIKE '" + jTextField_Nombre.getText() + "%'";
        model = new DefaultTableModel(null, titulos);
        //Connection conect = ClassConection.getConexion();
        try {
            st = (Statement) cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("CodAlumno");
                registros[1] = rs.getString("NombreAlumno");
                registros[2] = rs.getString("ApellidosAlumno");
                registros[3] = rs.getString("DNIAlumno");
                registros[4] = rs.getString("FechaNacimiento");
                registros[5] = rs.getString("DireccionAlumno");
                registros[6] = rs.getString("Sexo");
                registros[7] = rs.getString("TelefonoAlumno");
                registros[8] = rs.getString("EstadoAlumno");

                model.addRow(registros);
            }
            jTable_Alumnos.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Error al Buscar Datos : " + ex.getMessage());
        }
    }//GEN-LAST:event_jTextField_NombreKeyReleased


