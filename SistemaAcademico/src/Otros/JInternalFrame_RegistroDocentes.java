    public void cargar() 
    {

        DefaultTableModel tabla = new DefaultTableModel();
        try 
        {
            tabla.addColumn("Codigo Alumno");
            tabla.addColumn("Nombres");
            tabla.addColumn("Apellidos");
            tabla.addColumn("DNI");
            tabla.addColumn("Fecha Nacimiento");            
            tabla.addColumn("Direccion");            
            tabla.addColumn("Sexo");
            tabla.addColumn("Telefono");
            tabla.addColumn("Estado del Alumno");
            cts = cn.prepareCall("{call sp_mostraralumnos}");
            r = cts.executeQuery();
            while (r.next()) 
            {
                Object dato[] = new Object[9];
                for (int i = 0; i < 9; i++) {
                    dato[i] = r.getString(i + 1);
                }
                tabla.addRow(dato);
            }
            this.jTable_Alumnos.setModel(tabla);

        } catch (Exception e) {
        }
    }

