    private void jTextField_NombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_NombreKeyReleased
        // TODO add your handling code here:
        String[] titulos = {"Codigo Docente", "Codigo Escuela Prof.","Nombres","Apellidos","DNI","Sexo","Fecha Nacimiento","Grado Academico", "Categoria Docente","Condicion Docente"};
        String[] registros = new String[200];
        String sql = "SELECT * FROM tdocente WHERE NombreDocente LIKE '" + jTextField_Nombre.getText() + "%'";
        model = new DefaultTableModel(null, titulos);
        //Connection conect = ClassConection.getConexion();
        try {
            st = (Statement) cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("CodDocente");
                registros[1] = rs.getString("CodEscuelaProfesional");
                registros[2] = rs.getString("NombreDocente");
                registros[3] = rs.getString("ApellidosDocente");
                registros[4] = rs.getString("DNIDocente");
                registros[5] = rs.getString("Sexo");
                registros[6] = rs.getString("FechaNacimiento");
                registros[7] = rs.getString("GradoAcademico");
                registros[8] = rs.getString("CategoriaDocente");
                registros[9] = rs.getString("CondicionDocente");

                model.addRow(registros);
            }
            jTable_Docentes.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Error al Buscar Datos: " + ex.getMessage());
        }
    }//GEN-LAST:event_jTextField_NombreKeyReleased


