    public void cargar() 
    {

        DefaultTableModel tabla = new DefaultTableModel();
        try 
        {
            tabla.addColumn("Codigo Docente");
            tabla.addColumn("Codigo Escuela Profesional");
            tabla.addColumn("Nombres");
            tabla.addColumn("Apellidos");
            tabla.addColumn("DNI");
            tabla.addColumn("Sexo");
            tabla.addColumn("Fecha Nacimiento");            
            tabla.addColumn("Grado Academico");            
            tabla.addColumn("Categoria Docente");
            tabla.addColumn("Condicion Docente");
            cts = cn.prepareCall("{call sp_mostrardocentes}");
            r = cts.executeQuery();
            while (r.next()) 
            {
                Object dato[] = new Object[10];
                for (int i = 0; i < 10; i++) {
                    dato[i] = r.getString(i + 1);
                }
                tabla.addRow(dato);
            }
            this.jTable_Docentes.setModel(tabla);

        } catch (Exception e) {
        }
    }

